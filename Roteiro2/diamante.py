n = int(input())
cont = 1
for i in range(n, 0, -1):
    print ('%s%s' % (('.'*i) , '*'*cont) )
    cont +=2
    
for i in range(1, n+1):    
    cont -=2
    
    print ('%s%s' % (('.'*i) , '*'*cont) )
    
